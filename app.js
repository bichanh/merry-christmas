$(function() {
  var slide = function(object) {
    $(object).css({
      'marginLeft': '0%'
    })
    $(object).animate({
      'marginLeft': '100%'
    }, 10000, function() {
      slide(object);
    });
  };

  slide($('#outer'));
});
